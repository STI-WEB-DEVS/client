}

const handleUserAction = async (item) => {
  if (item.action === 'logout') {
    showLogoutModal.value = true;
  }
}

const confirmLogout = async () => {
  showLogoutModal.value = false;
  await handleLogout();
}

const cancelLogout = () => {
  showLogoutModal.value = false;
}
